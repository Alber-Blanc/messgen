CODEGEN_FILE_PREFIX = "// Code generated by messgen. DO NOT EDIT."

# Converts snake_case to camelCase (first letter lowercase)
def toDartName(name: str, capitalize_first: bool = True) -> str:
    parts = name.split('_')
    if capitalize_first:
        return "".join([x.capitalize() for x in parts])
    else:
        if len(parts) == 0:
            return name
        return parts[0] + "".join([x.capitalize() for x in parts[1:]])

# Converts CamelCase to snake_case for filenames
def toSnakeCase(name: str) -> str:
    import re
    # Insert underscore before uppercase letters (except at start)
    s1 = re.sub('(.)([A-Z][a-z]+)', r'\1_\2', name)
    # Insert underscore before uppercase letters followed by lowercase
    return re.sub('([a-z0-9])([A-Z])', r'\1_\2', s1).lower()

# Wraps messgen model
class ResolvedType:
    RESERVED_KEY_WORDS = {
        "abstract", "as", "assert", "async", "await", "break", "case", "catch",
        "class", "const", "continue", "covariant", "default", "deferred", "do",
        "dynamic", "else", "enum", "export", "extends", "extension", "external",
        "factory", "false", "final", "finally", "for", "Function", "get", "hide",
        "if", "implements", "import", "in", "interface", "is", "late", "library",
        "mixin", "new", "null", "of", "on", "operator", "part", "rethrow",
        "return", "set", "show", "static", "super", "switch", "sync", "this",
        "throw", "true", "try", "typedef", "var", "void", "while", "with", "yield",
    }

    def __init__(self, model: MessgenType, package: str):
        parsed = model.type.split("/")

        self._model = model
        self._package : list[str] = package.split('/') if package else []
        self._name = toDartName(parsed[-1], True)

    def name(self) -> str:
        return self._name

    def package_full(self) -> str:
        return "/".join(self._package)

    def package_name(self):
        if self._package:
            package_name = self._package[-1]
            if package_name in ResolvedType.RESERVED_KEY_WORDS:
                package_name = "_" + package_name
            return package_name
        return ""

    def imported(self, caller_pkg = None):
        return caller_pkg != self._package

    def reference(self, caller_pkg = None):
        # Dart doesn't need package prefix in same package
        return self._name

    def is_flat(self) -> bool:
        return False

    @abstractmethod
    def alignment(self):
        raise Exception("alignment is not defined for '%s'" % self._model.type)

    @abstractmethod
    def data_size(self):
        raise Exception("data_size is not defined for '%s'" % self._model.type)

    def type_size(self):
        return self.data_size()

    def model(self):
        return self._model

    @abstractmethod
    def render(self):
        raise Exception("There is no render method for type '%s'" % self._model.type)


class FieldGroup:
    def __init__(self):
        self.size = 0
        self.pad = 0
        self.fields = []
        self.flat = True

    def description(self) -> str:
        return ",".join([name for name, _ in self.fields])

    def add_field(self, name: str, field: ResolvedType):
        self.fields.append((name, field))
        self.flat = self.flat and field.is_flat()

        if self.size == None:
            return
        elif field.data_size() != None:
            self.size += field.data_size()
        else:
            self.size = None


class ResolvedBuiltin(ResolvedType):
    # Map messgen types to Dart types
    TYPE_MAP = {
        "int8": "int",
        "uint8": "int",
        "int16": "int",
        "uint16": "int",
        "int32": "int",
        "uint32": "int",
        "int64": "int",
        "uint64": "int",
        "float32": "double",
        "float64": "double",
        "bool": "bool",
        "string": "String",
        "bytes": "Uint8List",
    }

    def __init__(self, type_def: MessgenType, package: str):
        super().__init__(type_def, package)
        if type_def.type_class == TypeClass.bytes:
            self._name = "Uint8List"
        elif type_def.type in self.TYPE_MAP:
            self._name = self.TYPE_MAP[type_def.type]
        else:
            self._name = type_def.type

    def imported(self, caller_pkg = None):
        _ = caller_pkg
        return False

    def reference(self, caller_pkg=None):
        _ = caller_pkg
        return self._name

    def alignment(self):
        if isinstance(self._model, BasicType):
            if self._model.type_class in [TypeClass.scalar]:
                return self._model.size
            elif self._model.type_class in [TypeClass.string, TypeClass.bytes]:
                return 8
        raise Exception("Unknown builtin type class for '%s'" % self._model.type)

    def is_flat(self):
        return self._model.type_class == TypeClass.scalar

    def data_size(self):
        if isinstance(self._model, BasicType):
            if self._model.type_class in [TypeClass.scalar]:
                return self._model.size
            elif self._model.type_class in [TypeClass.string, TypeClass.bytes]:
                return None
        raise Exception("Unknown builtin type class for '%s'" % self._model.type)

    def type_size(self):
        if isinstance(self._model, BasicType):
            if self._model.type_class in [TypeClass.scalar]:
                return self._model.size
            elif self._model.type_class in [TypeClass.string, TypeClass.bytes]:
                return None
        raise Exception("Unknown builtin type class for '%s'" % self._model.type)

    def render(self):
        raise Exception("Render is not supported for ResolvedBuiltin")


class ResolvedSlice(ResolvedType):
    def __init__(self, model: ArrayType | VectorType, elem_type : ResolvedType, package: str):
        super().__init__(model, package)
        self._element = elem_type

    def imported(self, caller_pkg = None):
        return self._element.imported(caller_pkg)

    def reference(self, caller_pkg = None):
        return "List<%s>" % self._element.reference(caller_pkg)

    def alignment(self):
        if isinstance(self._model, ArrayType):
            return self._element.alignment()
        return 8

    def is_flat(self) -> bool:
        return False

    def type_size(self):
        if isinstance(self._model, ArrayType) and self._element.type_size():
            return self._model.array_size * self._element.type_size()
        return None

    def data_size(self):
        if isinstance(self._model, ArrayType) and self._element.data_size() != None:
            return self._element.data_size() * self._model.array_size
        return None

    def render(self):
        raise Exception("Render is not supported for ResolvedSlice")


class ResolvedMap(ResolvedType):
    def __init__(self, type_def: MapType, key: ResolvedType, value: ResolvedType, package : str):
        super().__init__(type_def, package)
        self._key = key 
        self._value = value 

    def imported(self, caller_pkg = None):
        return self._key.imported(caller_pkg) or self._value.imported(caller_pkg)

    def reference(self, caller_pkg = None):
        return "Map<%s, %s>" % (self._key.reference(caller_pkg), self._value.reference(caller_pkg))

    def is_flat(self) -> bool:
        return False

    def alignment(self):
        return 8

    def type_size(self):
        return None

    def data_size(self):
        return None

    def render(self):
        raise Exception("Render is not supported for ResolvedMap")


class ResolvedEnum(ResolvedType):
    def __init__(self, type_def: EnumType, base: ResolvedType, package: str):
        super().__init__(type_def, package)
        self._base = base

    def model(self):
        return cast(EnumType, self._model)
    
    def reference(self, caller_pkg = None):
        # Enums are represented as their enum class type in field declarations
        return self._name

    def alignment(self):
        return self._base.alignment()

    def data_size(self):
        return self._base.data_size()

    def type_size(self):
        return self._base.type_size()

    def is_flat(self):
        return True

    def render(self):
        model = self.model()
        
        yield f"{CODEGEN_FILE_PREFIX}\n"
        yield f"import 'dart:typed_data';\n"
        yield f"import 'package:messgen/messgen.dart';\n"
        
        if model.comment:
            yield f"/// {model.comment}"
        
        yield f"class {self._name} {{"
        yield f"  final int value;"
        yield ""
        yield f"  const {self._name}(this.value);"
        yield ""
        
        # Constants for enum values (now as instances of the enum class)
        for val in model.values:
            if val.comment:
                yield f"  /// {val.comment}"
            val_name = toDartName(val.name, False)
            yield f"  static const {self._name} {val_name} = {self._name}({val.value});"
        
        yield ""
        
        yield f"  @override"
        yield f"  bool operator ==(Object other) =>"
        yield f"      identical(this, other) ||"
        yield f"          other is {self._name} &&"
        yield f"              runtimeType == other.runtimeType &&"
        yield f"              value == other.value;"
        yield ""
        yield f"  @override"
        yield f"  int get hashCode => value.hashCode;"
        yield ""

        # 3. Generate Static List of all values
        all_const_names = [toDartName(val.name, False) for val in model.values]
        yield f"  static List<{self._name}> values = [{', '.join(all_const_names)}];"
        yield ""

        # 4. Generate toString() override
        yield f"  @override"
        yield f"  String toString() {{"
        yield f"    switch (value) {{"
        for val in model.values:
            val_name = toDartName(val.name, False)
            yield f"      case {val.value}: return '{val.name}';"
        yield f"      default: return 'unknown($value)';"
        yield f"    }}"
        yield f"  }}"
        yield f"}}"


class ResolvedBitset(ResolvedType):
    def __init__(self, type_def: BitsetType, base: ResolvedType, package: str):
        super().__init__(type_def, package)
        self._base = base

    def model(self):
        return cast(BitsetType, self._model)
    
    def reference(self, caller_pkg = None):
        # Bitsets are represented as their bitset class type in field declarations
        return self._name

    def alignment(self):
        return self._base.alignment()

    def data_size(self):
        return self._base.data_size()

    def type_size(self):
        return self._base.type_size()

    def is_flat(self):
        return True

    def render(self):
        model = self.model()
        
        yield f"{CODEGEN_FILE_PREFIX}\n"
        yield f"import 'dart:typed_data';\n"
        yield f"import 'package:messgen/messgen.dart';\n"
        
        if model.comment:
            yield f"/// {model.comment}"
        
        yield f"class {self._name} {{"
        yield f"  late int value;"
        yield ""
        yield f"  {self._name}(this.value);"
        yield ""
        
        # Bit constants
        for bit in model.bits:
            if bit.comment:
                yield f"  /// {bit.comment}"
            bit_name = toDartName(bit.name, False)
            yield f"  static const int {bit_name} = {bit.offset};"
        
        yield ""
        
        # has method
        yield f"  bool has(int bit) {{"
        yield f"    return (value & (1 << bit)) != 0;"
        yield f"  }}"
        yield ""

        # set method - fluent mutable
        yield f"  {self._name} set(int bit) {{"
        yield f"    value |= (1 << bit);"
        yield f"    return this;"
        yield f"  }}"
        yield ""

        # clear method - fluent mutable
        yield f"  {self._name} clear(int bit) {{"
        yield f"    value &= ~(1 << bit);"
        yield f"    return this;"
        yield f"  }}"
        yield ""
        
        # toString method
        yield f"  @override"
        yield f"  String toString() {{"
        yield f"    final bits = <String>[];"
        for bit in model.bits:
            bit_name = toDartName(bit.name, False)
            yield f"    if (has({bit_name})) bits.add('{bit.name}');"
        yield f"    return bits.isEmpty ? '{{}}' : '{{${{bits.join(' | ')}}}}';"
        yield f"  }}"
        yield f"}}"


class ResolvedStruct(ResolvedType):
    def __init__(self, type_def: StructType, hash: int | None, package: str):
        super().__init__(type_def, package)
        self._hash = hash
        self._fields: list[tuple[str, ResolvedType]] = []
        self._field_defs: dict[str, str | None] = {}  # Store field comments

    def model(self):
        return cast(StructType, self._model)

    def add_field(self, name: str, field: ResolvedType, comment: str | None = None):
        self._fields.append((name, field))
        self._field_defs[name] = comment

    def fields(self):
        return self._fields

    def alignment(self):
        return 8

    def data_size(self):
        total = 0
        for _, field in self._fields:
            size = field.data_size()
            if size is None:
                return None
            total += size
        return total if total > 0 else 0

    def is_flat(self) -> bool:
        for _, field in self._fields:
            if not field.is_flat():
                return False
        return True

    def renderSize(self, name:str, cur: ResolvedType, step = 0):
        if cur._model.type_class == TypeClass.string:
            yield f"    result += 4 + utf8.encode({name}).length;"
        elif cur._model.type_class == TypeClass.bytes:
            yield f"    result += 4 + {name}.length;"
        elif cur._model.type_class == TypeClass.struct:
            yield f"    result += {name}.serializedSize();"
        elif isinstance(cur, ResolvedEnum):
            # Enums use their base type size
            yield from self.renderSize(name, cur._base, step)
        elif isinstance(cur, ResolvedBitset):
            # Bitsets use their base type size
            yield from self.renderSize(name, cur._base, step)
        elif isinstance(cur, ResolvedBuiltin):
            yield f"    result += {cur.type_size()};"
        elif isinstance(cur, ResolvedSlice):
            elem_idx = f"i{step}"
            elem_name = f"elem{step}"
            if cur._model.type_class != TypeClass.array:
                yield f"    result += 4;"
            if cur._element.data_size() != None and cur._element.is_flat():
                yield f"    result += {name}.length * {cur._element.type_size()};"
            else:
                yield f"    for (final {elem_name} in {name}) {{"
                yield from self.renderSize(elem_name, cur._element, step+1)
                yield f"    }}"
        elif isinstance(cur, ResolvedMap):
            yield f"    result += 4;"
            yield f"    {name}.forEach((key, value) {{"
            yield f"      // Count key"
            yield from self.renderSize("key", cur._key, step+1)
            yield f"      // Count value"
            yield from self.renderSize("value", cur._value, step+1)
            yield f"    }});"

    def renderSerialize(self, name: str, cur: ResolvedType, step = 0):
        if cur._model.type_class == TypeClass.string:
            yield f"    writer.writeString({name});"
        elif cur._model.type_class == TypeClass.bytes:
            yield f"    writer.writeBytes({name});"
        elif cur._model.type_class == TypeClass.struct:
            yield f"    final bytesWritten = {name}.serialize(Uint8List.sublistView(output, writer.offset));"
            yield f"    writer.advance(bytesWritten);"
        elif isinstance(cur, ResolvedEnum):
            # Enums are serialized by accessing their .value field
            yield from self.renderSerialize(f"{name}.value", cur._base, step)
        elif isinstance(cur, ResolvedBitset):
            # Bitsets are serialized by accessing their .value field
            yield from self.renderSerialize(f"{name}.value", cur._base, step)
        elif isinstance(cur, ResolvedBuiltin):
            type_name = cur._model.type
            if type_name == "int8":
                yield f"    writer.writeInt8({name});"
            elif type_name == "uint8":
                yield f"    writer.writeUint8({name});"
            elif type_name == "int16":
                yield f"    writer.writeInt16({name});"
            elif type_name == "uint16":
                yield f"    writer.writeUint16({name});"
            elif type_name == "int32":
                yield f"    writer.writeInt32({name});"
            elif type_name == "uint32":
                yield f"    writer.writeUint32({name});"
            elif type_name == "int64":
                yield f"    writer.writeInt64({name});"
            elif type_name == "uint64":
                yield f"    writer.writeUint64({name});"
            elif type_name == "float32":
                yield f"    writer.writeFloat32({name});"
            elif type_name == "float64":
                yield f"    writer.writeFloat64({name});"
            elif type_name == "bool":
                yield f"    writer.writeBool({name});"
        elif isinstance(cur, ResolvedSlice):
            elem_name = f"elem{step}"
            if cur._model.type_class != TypeClass.array:
                yield f"    writer.writeUint32({name}.length);"
            
            if cur._element.data_size() != None and cur._element.is_flat():
                # For flat types, we can serialize efficiently
                yield f"    for (final {elem_name} in {name}) {{"
                yield from self.renderSerialize(elem_name, cur._element, step+1)
                yield f"    }}"
            else:
                yield f"    for (final {elem_name} in {name}) {{"
                yield from self.renderSerialize(elem_name, cur._element, step+1)
                yield f"    }}"
        elif isinstance(cur, ResolvedMap):
            yield f"    writer.writeUint32({name}.length);"
            yield f"    {name}.forEach((key, value) {{"
            yield f"      // Write key"
            yield from self.renderSerialize("key", cur._key, step+1)
            yield f"      // Write value"
            yield from self.renderSerialize("value", cur._value, step+1)
            yield f"    }});"

    def renderDeserialize(self, name: str, cur: ResolvedType, step = 0):
        if cur._model.type_class == TypeClass.string:
            yield f"    {name} = reader.readString();"
        elif cur._model.type_class == TypeClass.bytes:
            yield f"    {name} = reader.readBytes();"
        elif cur._model.type_class == TypeClass.struct:
            yield f"    final bytesRead = {name}.deserialize(Uint8List.sublistView(input, reader.offset));"
            yield f"    reader.advance(bytesRead);"
        elif isinstance(cur, ResolvedEnum):
            # Enums are deserialized by wrapping the int value in the enum constructor
            temp_var = f"temp{step}"
            yield f"    int {temp_var};"
            yield from self.renderDeserialize(temp_var, cur._base, step)
            yield f"    {name} = {cur.reference()}({temp_var});"
        elif isinstance(cur, ResolvedBitset):
            # Bitsets are deserialized by wrapping the int value in the bitset constructor
            temp_var = f"temp{step}"
            yield f"    int {temp_var};"
            yield from self.renderDeserialize(temp_var, cur._base, step)
            yield f"    {name} = {cur.reference()}({temp_var});"
        elif isinstance(cur, ResolvedBuiltin):
            type_name = cur._model.type
            if type_name == "int8":
                yield f"    {name} = reader.readInt8();"
            elif type_name == "uint8":
                yield f"    {name} = reader.readUint8();"
            elif type_name == "int16":
                yield f"    {name} = reader.readInt16();"
            elif type_name == "uint16":
                yield f"    {name} = reader.readUint16();"
            elif type_name == "int32":
                yield f"    {name} = reader.readInt32();"
            elif type_name == "uint32":
                yield f"    {name} = reader.readUint32();"
            elif type_name == "int64":
                yield f"    {name} = reader.readInt64();"
            elif type_name == "uint64":
                yield f"    {name} = reader.readUint64();"
            elif type_name == "float32":
                yield f"    {name} = reader.readFloat32();"
            elif type_name == "float64":
                yield f"    {name} = reader.readFloat64();"
            elif type_name == "bool":
                yield f"    {name} = reader.readBool();"
        elif isinstance(cur, ResolvedSlice):
            elem_name = f"elem{step}"
            if cur._model.type_class == TypeClass.array:
                size = cast(ArrayType, cur._model).array_size
                yield f"    for (int i{step} = 0; i{step} < {size}; i{step}++) {{"
                if cur._element._model.type_class == TypeClass.struct:
                    yield f"      final {elem_name} = {cur._element.reference()}.empty();"
                    yield from self.renderDeserialize(elem_name, cur._element, step+1)
                    yield f"      {name}[i{step}] = {elem_name};"
                elif isinstance(cur._element, ResolvedSlice):
                    # Element is an array/list - need to declare and let recursion initialize
                    yield f"      {cur._element.reference()} {elem_name};"
                    yield from self.renderDeserialize(elem_name, cur._element, step+1)
                    yield f"      {name}[i{step}] = {elem_name};"
                else:
                    yield f"      {cur._element.reference()} {elem_name};"
                    yield from self.renderDeserialize(elem_name, cur._element, step+1)
                    yield f"      {name}[i{step}] = {elem_name};"
                yield f"    }}"
            else:
                yield f"    final size{step} = reader.readUint32();"
                yield f"    {name} = [];"
                yield f"    for (int i{step} = 0; i{step} < size{step}; i{step}++) {{"
                if cur._element._model.type_class == TypeClass.struct:
                    yield f"      final {elem_name} = {cur._element.reference()}.empty();"
                    yield from self.renderDeserialize(elem_name, cur._element, step+1)
                    yield f"      {name}.add({elem_name});"
                elif isinstance(cur._element, ResolvedSlice) and isinstance(cur._element._model, ArrayType):
                    # Element is a fixed-size array - need to pre-allocate before the loop
                    array_size = cur._element._model.array_size
                    if cur._element._element._model.type_class == TypeClass.struct:
                        # Fixed array of structs - pre-allocate with empty instances
                        yield f"      {cur._element.reference()} {elem_name} = List.generate({array_size}, (_) => {cur._element._element.reference()}.empty());"
                    elif isinstance(cur._element._element, ResolvedSlice):
                        # Fixed array of lists - pre-allocate with empty lists
                        yield f"      {cur._element.reference()} {elem_name} = List.generate({array_size}, (_) => []);"
                    else:
                        # Fixed array of primitives - pre-allocate with default values
                        yield f"      {cur._element.reference()} {elem_name} = List.filled({array_size}, {self._getDefaultValue(cur._element._element)});"
                    yield from self.renderDeserialize(elem_name, cur._element, step+1)
                    yield f"      {name}.add({elem_name});"
                elif isinstance(cur._element, ResolvedSlice):
                    # Element is a dynamic vector - need to declare and let recursion initialize
                    yield f"      {cur._element.reference()} {elem_name};"
                    yield from self.renderDeserialize(elem_name, cur._element, step+1)
                    yield f"      {name}.add({elem_name});"
                else:
                    yield f"      {cur._element.reference()} {elem_name};"
                    yield from self.renderDeserialize(elem_name, cur._element, step+1)
                    yield f"      {name}.add({elem_name});"
                yield f"    }}"
        elif isinstance(cur, ResolvedMap):
            yield f"    final size{step} = reader.readUint32();"
            yield f"    {name} = {{}};"
            yield f"    for (int i{step} = 0; i{step} < size{step}; i{step}++) {{"
            yield f"      {cur._key.reference()} key;"
            yield from self.renderDeserialize("key", cur._key, step+1)
            # For struct values, we need to create an instance before deserializing
            if cur._value._model.type_class == TypeClass.struct:
                yield f"      final value = {cur._value.reference()}.empty();"
                yield from self.renderDeserialize("value", cur._value, step+1)
            else:
                yield f"      {cur._value.reference()} value;"
                yield from self.renderDeserialize("value", cur._value, step+1)
            yield f"      {name}[key] = value;"
            yield f"    }}"

    def render(self):
        model = self.model()
        
        yield f"{CODEGEN_FILE_PREFIX}\n"
        yield f"import 'dart:typed_data';\n"
        yield f"import 'dart:convert';\n"
        yield f"import 'package:messgen/messgen.dart';\n"
        
        # Helper function to collect all types that need imports
        def collect_import_types(resolved_type, imports_set):
            """Recursively collect all struct/enum/bitset types that need imports"""
            if resolved_type._model.type_class in [TypeClass.enum, TypeClass.bitset]:
                # Always import enums and bitsets
                pkg_tuple = tuple(resolved_type._package) if resolved_type._package else ()
                imports_set.add((pkg_tuple, resolved_type._name))
            elif resolved_type._model.type_class == TypeClass.struct:
                # Import structs (even in same package for consistency)
                pkg_tuple = tuple(resolved_type._package) if resolved_type._package else ()
                imports_set.add((pkg_tuple, resolved_type._name))
            elif isinstance(resolved_type, ResolvedSlice):
                # Recursively check element type for arrays/vectors
                collect_import_types(resolved_type._element, imports_set)
            elif isinstance(resolved_type, ResolvedMap):
                # Recursively check key and value types for maps
                collect_import_types(resolved_type._key, imports_set)
                collect_import_types(resolved_type._value, imports_set)
        
        # Import dependencies
        imported_packages = set()
        for _, field in self._fields:
            collect_import_types(field, imported_packages)
        
        for pkg_tuple, type_name in sorted(imported_packages):
            # Convert type name to snake_case for filename
            filename = toSnakeCase(type_name) + "_gen.dart"
            
            # Calculate relative path from this file's package to the imported type's package
            if pkg_tuple and pkg_tuple != tuple(self._package):
                # Different package - calculate relative import path
                # self._package and pkg_tuple are tuples of path components
                # Convert to strings for path calculation
                from_path = "/".join(self._package) if self._package else ""
                to_path = "/".join(pkg_tuple) if pkg_tuple else ""
                
                # Use posixpath to calculate relative path
                if from_path and to_path:
                    rel_path = posixpath.relpath(to_path, start=from_path)
                    yield f"import '{rel_path}/{filename}';\n"
                else:
                    # Fallback to just the filename
                    yield f"import '{filename}';\n"
            else:
                # Same package - import by filename only
                yield f"import '{filename}';\n"
        
        if model.comment:
            yield f"/// {model.comment}"
        
        yield f"class {self._name} implements Serializable {{"
        
        # Fields (no default values since we use required constructor parameters)
        for field_name, field in self._fields:
            dart_field_name = toDartName(field_name, False)
            field_type = field.reference(self._package)
            
            # Get comment from stored field definition
            field_comment = self._field_defs.get(field_name)
            if field_comment:
                yield f"  /// {field_comment}"
            yield f"  {field_type} {dart_field_name};"
        
        yield ""
        
        # Constructor with required named parameters
        if self._fields:
            yield f"  {self._name}({{"
            for field_name, field in self._fields:
                dart_field_name = toDartName(field_name, False)
                yield f"    required this.{dart_field_name},"
            yield f"  }});"
        else:
            yield f"  {self._name}();"
        
        yield ""
        # Add a factory method for creating empty instances (used in deserialization)
        # This is needed even for empty structs to keep API consistent
        yield f"  factory {self._name}.empty() {{"
        if self._fields:
            yield f"    return {self._name}("
            for field_name, field in self._fields:
                dart_field_name = toDartName(field_name, False)
                field_type = field.reference(self._package)
                
                # Get default value for empty factory
                default_val = "0"
                if isinstance(field, ResolvedBuiltin):
                    if field._model.type == "bool":
                        default_val = "false"
                    elif field._model.type_class == TypeClass.scalar:
                        default_val = "0"
                    elif field._model.type == "string":
                        default_val = "''"
                    elif field._model.type_class == TypeClass.bytes:
                        default_val = "Uint8List(0)"
                elif isinstance(field, ResolvedSlice):
                    default_val = "[]"
                elif isinstance(field, ResolvedMap):
                    default_val = "{}"
                elif field._model.type_class == TypeClass.struct:
                    default_val = f"{field_type}.empty()"
                elif field._model.type_class == TypeClass.enum:
                    default_val = f"{field_type}(0)"
                elif field._model.type_class == TypeClass.bitset:
                    default_val = f"{field_type}(0)"
                
                yield f"      {dart_field_name}: {default_val},"
            yield f"    );"
        else:
            yield f"    return {self._name}();"
        yield f"  }}"
        yield ""
        
        # serializedSize method
        yield f"  @override"
        yield f"  int serializedSize() {{"
        yield f"    int result = 0;"
        for field_name, field in self._fields:
            dart_field_name = toDartName(field_name, False)
            yield from self.renderSize(dart_field_name, field)
        yield f"    return result;"
        yield f"  }}"
        yield ""
        
        # serialize method
        yield f"  @override"
        yield f"  int serialize(Uint8List output) {{"
        yield f"    final writer = BufferWriter(output);"
        for field_name, field in self._fields:
            dart_field_name = toDartName(field_name, False)
            yield from self.renderSerialize(dart_field_name, field)
        yield f"    return writer.offset;"
        yield f"  }}"
        yield ""
        
        # deserialize method
        yield f"  @override"
        yield f"  int deserialize(Uint8List input) {{"
        yield f"    final reader = BufferReader(input);"
        field_index = 0
        for field_name, field in self._fields:
            dart_field_name = toDartName(field_name, False)
            # For arrays, we need to initialize them first
            if isinstance(field, ResolvedSlice) and isinstance(field._model, ArrayType):
                size = field._model.array_size
                if field._element._model.type_class == TypeClass.struct:
                    # Use List.generate to avoid shared references
                    yield f"    {dart_field_name} = List.generate({size}, (_) => {field._element.reference()}.empty());"
                elif isinstance(field._element, ResolvedSlice):
                    # Element is a list - use List.generate to create empty lists
                    yield f"    {dart_field_name} = List.generate({size}, (_) => []);"
                else:
                    # For primitives, List.filled is safe
                    yield f"    {dart_field_name} = List.filled({size}, {self._getDefaultValue(field._element)});"
            # For structs, initialize them
            elif field._model.type_class == TypeClass.struct:
                yield f"    {dart_field_name} = {field.reference(self._package)}.empty();"
            
            # Pass field_index as the starting step to ensure unique variable names
            yield from self.renderDeserialize(dart_field_name, field, field_index * 100)
            field_index += 1
        yield f"    return reader.offset;"
        yield f"  }}"
        yield ""
        
        # hash method
        yield f"  @override"
        yield f"  BigInt hash() {{"
        if self._hash is not None:
            # Use BigInt for large hash values
            yield f"    return BigInt.parse('{self._hash}');"
        else:
            yield f"    return BigInt.zero;"
        yield f"  }}"
        yield f"}}"

    def _getDefaultValue(self, field: ResolvedType) -> str:
        """Get default value for a type"""
        if isinstance(field, ResolvedBuiltin):
            if field._model.type == "bool":
                return "false"
            elif field._model.type_class == TypeClass.scalar:
                return "0"
            elif field._model.type == "string":
                return "''"
        elif field._model.type_class in [TypeClass.enum, TypeClass.bitset]:
            return "0"
        return "null"


def render_protocol(proto_name: str, proto_class_name: str, proto_full_path: str, proto: Protocol, 
                   resolved: dict[str, ResolvedType], types: dict[str, MessgenType]):
    """Generate protocol dispatcher code"""
    
    # Compute protocol hash
    proto_hash = 0
    message_hashes = {}
    for msg_id, msg in proto.messages.items():
        type_hash = hash_type(types[msg.type], types)
        msg_hash = hash_message(msg) ^ type_hash if type_hash else 0
        proto_hash ^= msg_hash
        # Store message hash as-is for BigInt
        message_hashes[msg_id] = msg_hash
    
    yield f"{CODEGEN_FILE_PREFIX}\n"
    yield f"import 'dart:typed_data';\n"
    yield f"import 'package:messgen/messgen.dart';\n"
    
    # Import message types with proper relative paths
    # Group imports by their type folder
    type_imports = {}  # Maps type folder to set of filenames
    msg_type_names = {}  # Map message IDs to type class names
    
    for msg_id, msg in proto.messages.items():
        if msg.type in types:
            msg_type = types[msg.type]
            if msg_type.type_class == TypeClass.struct:
                if msg.type in resolved:
                    resolved_type = resolved[msg.type]
                    msg_type_names[msg_id] = resolved_type.name()
                    
                    # Extract the type folder from the full type path
                    # e.g., "mynamespace/types/subspace/complex_struct" -> "mynamespace/types/subspace"
                    #       "mynamespace/types/simple_struct" -> "mynamespace/types"
                    type_path_parts = msg.type.split("/")
                    if len(type_path_parts) > 2:
                        # Has namespace/types or namespace/types/subspace
                        type_folder = "/".join(type_path_parts[:-1])
                    else:
                        type_folder = msg.type.rsplit("/", 1)[0]
                    
                    # Calculate relative path from protocol directory to type directory
                    rel_path = posixpath.relpath(type_folder, start=proto_full_path)
                    
                    # Use the actual generated filename with snake_case
                    filename = toSnakeCase(resolved_type.name()) + "_gen.dart"
                    
                    if rel_path not in type_imports:
                        type_imports[rel_path] = set()
                    type_imports[rel_path].add(filename)
    
    # Generate import statements
    for rel_path in sorted(type_imports.keys()):
        for filename in sorted(type_imports[rel_path]):
            yield f"import '{rel_path}/{filename}';\n"

    yield ""
    
    # Message handler type - must be outside class
    yield f"typedef MessageHandler = Future<void> Function(dynamic context, Uint8List body);"
    yield ""
    
    yield f"class {proto_class_name}Protocol {{"
    yield f"  static const int id = {proto.proto_id};"
    yield f"  static const String name = '{proto.name}';"
    yield f"  static final BigInt hash = BigInt.parse('{proto_hash}');"
    yield ""
    
    # Message IDs and hashes
    for msg_id, msg in proto.messages.items():
        msg_name = toDartName(msg.name, False)
        yield f"  static const int {msg_name}Id = {msg_id};"
        yield f"  static final BigInt {msg_name}Hash = BigInt.parse('{message_hashes[msg_id]}');"
    
    yield ""
    
    # Message ID to constructor map
    yield f"  static final Map<int, Serializable Function()> messageIdToConstructor = {{"
    for msg_id, msg in proto.messages.items():
        if msg_id in msg_type_names:
            type_name = msg_type_names[msg_id]
            yield f"    {msg_id}: () => {type_name}.empty(),"
    yield f"  }};"
    yield ""
    
    # Handlers map
    yield f"  final Map<int, MessageHandler> _handlers = {{}};"
    yield ""
    
    # Set handler methods
    for msg_id, msg in proto.messages.items():
        msg_name = toDartName(msg.name, True)
        camel_msg_name = toDartName(msg.name, False)
        yield f"  void setHandler{msg_name}(MessageHandler handler) {{"
        yield f"    _handlers[{camel_msg_name}Id] = handler;"
        yield f"  }}"
        yield ""
    
    # Dispatch method
    yield f"  Future<void> dispatch(dynamic context, int messageId, Uint8List body) async {{"
    yield f"    final handler = _handlers[messageId];"
    yield f"    if (handler == null) {{"
    yield f"      throw MessgenException('No handler found for message id $messageId');"
    yield f"    }}"
    yield f"    await handler(context, body);"
    yield f"  }}"
    yield ""
    
    # Get message name
    yield f"  static String messageName(int messageId) {{"
    yield f"    switch (messageId) {{"
    for msg_id, msg in proto.messages.items():
        yield f"      case {msg_id}: return '{msg.name}';"
    yield f"      default: return 'unknown';"
    yield f"    }}"
    yield f"  }}"
    yield f"}}"


class DartGenerator:
    PROTO_TYPE_VAR_TYPE = "int"

    def __init__(self, options: dict):
        self._options: dict = options
        self._types: dict[str, MessgenType] = {}
        self._resolved: dict[str, ResolvedType] = {}
        self._out_dir: Path | None = None
        self._protocols: dict[str, Protocol] = {}

    def generate_type(self, out_dir: Path, typename: str, ident=0) -> ResolvedType:
        if typename in self._resolved:
            return self._resolved[typename]
        elif typename not in self._types:
            raise Exception("Unknown type: %s" % (typename))

        resolved : ResolvedType | None = None
        type_def = self._types[typename]

        # Determine package for non-builtin types
        if type_def.type_class not in [TypeClass.scalar, TypeClass.bytes, TypeClass.string]:
            package = f"{out_dir.name}/{type_def.type}"
            package = package.rsplit("/", 1)[0]
        else:
            package = ""

        if type_def.type_class == TypeClass.array or type_def.type_class == TypeClass.vector:
            slice_type = cast(ArrayType | VectorType, type_def)
            tmp = self.generate_type(out_dir, slice_type.element_type, ident+2)
            resolved = ResolvedSlice(slice_type, tmp, package)
        elif type_def.type_class == TypeClass.map:
            map_type = cast(MapType, type_def)
            key_t = self.generate_type(out_dir, map_type.key_type, ident+2)
            val_t = self.generate_type(out_dir, map_type.value_type, ident+2)
            resolved = ResolvedMap(map_type, key_t, val_t, package)
        elif type_def.type_class == TypeClass.enum:
            enum_type = cast(EnumType, type_def)
            base = self.generate_type(out_dir, enum_type.base_type, ident+2)
            resolved = ResolvedEnum(enum_type, base, package)
        elif type_def.type_class == TypeClass.bitset:
            bitset_type = cast(BitsetType, type_def)
            base = self.generate_type(out_dir, bitset_type.base_type, ident+2)
            resolved = ResolvedBitset(bitset_type, base, package)
        elif type_def.type_class == TypeClass.struct:
            struct_hash: int | None = hash_type(type_def, self._types)

            struct_type = cast(StructType, type_def)
            resolved_struct = ResolvedStruct(struct_type, struct_hash, package)

            for field in struct_type.fields:
                fresolved = self.generate_type(out_dir, field.type, ident+2)
                resolved_struct.add_field(field.name, fresolved, field.comment)

            resolved = resolved_struct
        elif isinstance(type_def, BasicType):
            resolved = ResolvedBuiltin(type_def, package)
        else:
            raise Exception("Type %s is not struct/enum/bitset: %s" % (typename, type_def))

        self._resolved[typename] = resolved
        return resolved

    def generate_types(self, out_dir: Path, types: dict[str, MessgenType]) -> None:
        self._types = types
        self._out_dir = out_dir

        for type_name, _ in types.items():
            type_obj = self.generate_type(out_dir, type_name)

            # Only struct/enum/bitset gets generated
            output = None
            if type_obj._model.type_class in [TypeClass.struct, TypeClass.enum, TypeClass.bitset]:
                # Get relative path from type
                pkg = "/".join(type_obj._package).removeprefix(f"{out_dir.name}/")
                pkg = pkg.removeprefix("/")
                output = out_dir / pathlib.Path(pkg)
                # Convert type name to snake_case for filename
                filename = toSnakeCase(type_obj._name) + "_gen.dart"
                output = output.joinpath(filename)
            else:
                continue

            output.parent.mkdir(parents=True, exist_ok=True)
            with open(output, 'w') as file:
                for line in type_obj.render():
                    print(line, file=file)
        # Generate barrel files for types after all types are generated
        self._generate_type_barrel_files(out_dir, types)

    def generate_protocols(self, out_dir: Path, types: dict[str, MessgenType], protocols: dict[str, Protocol]) -> None:
        self.generate_types(out_dir, types)

        self._protocols = protocols
        self._out_dir = out_dir
        
        for proto_full_name, proto_def in protocols.items():
            proto_name = proto_full_name.split("/")[-1]

            if proto_name in ResolvedType.RESERVED_KEY_WORDS:
                proto_name = "_" + proto_name

            proto_class_name = toDartName(proto_name, True)
            file_name = out_dir / proto_full_name / "proto_gen.dart"

            file_name.parent.mkdir(parents=True, exist_ok=True)

            with open(file_name, 'w') as file:
                for line in render_protocol(proto_name, proto_class_name, proto_full_name, proto_def, self._resolved, self._types):
                    print(line, file=file)
                file.close()

            # Format the generated Dart code
            try:
                subprocess.call(["dart", "format", file_name], text=True)
            except:
                pass  # dart format might not be available
        
        # Generate barrel files for protocols after all protocols are generated
        self._generate_protocol_barrel_files(out_dir, protocols)
    
    def _generate_type_barrel_files(self, out_dir: Path, types: dict[str, MessgenType]) -> None:
        """Generate barrel files (export files) for type folders"""
        from collections import defaultdict
        
        # Group types by folder
        types_by_folder = defaultdict(list)
        for typename, type_def in types.items():
            if type_def.type_class in [TypeClass.struct, TypeClass.enum, TypeClass.bitset]:
                if typename in self._resolved:
                    resolved_type = self._resolved[typename]
                    # Extract folder path
                    type_path_parts = typename.split("/")
                    if len(type_path_parts) > 2:
                        folder = "/".join(type_path_parts[:-1])
                    else:
                        folder = typename.rsplit("/", 1)[0]
                    
                    filename = toSnakeCase(resolved_type.name()) + "_gen.dart"
                    types_by_folder[folder].append(filename)
        
        # Generate barrel file for each types folder
        for folder, filenames in types_by_folder.items():
            barrel_path = out_dir / folder / "index.dart"
            barrel_path.parent.mkdir(parents=True, exist_ok=True)
            
            with open(barrel_path, 'w') as f:
                f.write(f"{CODEGEN_FILE_PREFIX}\n\n")
                f.write("// Barrel file - exports all types in this folder\n\n")
                for filename in sorted(filenames):
                    f.write(f"export '{filename}';\n")
            
            # Format the barrel file
            try:
                subprocess.call(["dart", "format", barrel_path], text=True)
            except:
                pass
    
    def _generate_protocol_barrel_files(self, out_dir: Path, protocols: dict[str, Protocol]) -> None:
        """Generate barrel files (export files) for protocol folders"""
        # Generate barrel files for protocols
        for proto_full_name in protocols.keys():
            proto_dir = out_dir / proto_full_name
            barrel_path = proto_dir / "index.dart"
            
            with open(barrel_path, 'w') as f:
                f.write(f"{CODEGEN_FILE_PREFIX}\n\n")
                f.write("// Barrel file - exports protocol\n\n")
                f.write("export 'proto_gen.dart';\n")
            
            # Format the barrel file
            try:
                subprocess.call(["dart", "format", barrel_path], text=True)
            except:
                pass
    
    def generate(self, out_dir: Path, types: dict[str, MessgenType], protocols: dict[str, Protocol]) -> None:
        """Generate all Dart code including types, protocols, and barrel files"""
        self.generate_types(out_dir, types)
        self.generate_protocols(out_dir, protocols)
